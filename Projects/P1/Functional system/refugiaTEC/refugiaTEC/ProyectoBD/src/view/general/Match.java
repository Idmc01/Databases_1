    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            SysConexion con = new SysConexion();
            Connection connection = con.obtConexion();
            CallableStatement callableStatement;

            DefaultTableModel modelo = new DefaultTableModel();
            this.jTable1.setModel(modelo);

            // El procedimiento GetPetsByRescuer devuelve un cursor, por lo que debes usar "CallableStatement" en lugar de "PreparedStatement"
            callableStatement = connection.prepareCall("{call ? := findmatches(?, ?, ?, ?)}");
            callableStatement.registerOutParameter(1, OracleTypes.CURSOR);
            callableStatement.setInt(2, 4);
            callableStatement.setInt(3, 4);
            callableStatement.setString(4, "4");
            callableStatement.setString(5, "4");
            

            callableStatement.execute();
            ResultSet rs = (ResultSet) callableStatement.getObject(1);
            ResultSetMetaData rsMd = rs.getMetaData();

            // La cantidad de columnas que tiene la consulta
            int cantidadColumnas = rsMd.getColumnCount();

            // Establecer como cabeceras el nombre de las columnas
            for (int i = 1; i <= cantidadColumnas; i++) {
                modelo.addColumn(rsMd.getColumnLabel(i));
            }

            // Creando las filas para el JTable
            while (rs.next()) {
                Object[] fila = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                modelo.addRow(fila);
            }

            rs.close();
            connection.close();
        } catch (SQLException ex) {
            ex.printStackTrace(); // CambiÃ© ex.getErrorCode() a ex.printStackTrace() para mostrar el mensaje completo de error.
        }
    }//GEN-LAST:event_jButton1ActionPerformed

